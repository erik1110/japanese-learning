CATEGORIES = [
    ("ono-voice", "笑聲・說話", "🗣️", VOICE),
    ("ono-animal", "動物叫聲", "🐾", ANIMAL),
    ("ono-sound", "自然・物音", "💧", SOUND),
    ("ono-taste", "口感・吃喝", "🍜", TASTE),
    ("ono-feeling", "心情・情緒", "💓", FEELING),
    ("ono-body", "身體・狀態", "😵", BODY),
]


def build(items):
    return [
        {"word": w, "kana": w, "meaning_zh": m, "example_jp": ej, "example_zh": ez}
        for (w, m, ej, ez) in items
    ]


def main():
    data = {
        "level": "擬音",
        "name_zh": "擬聲・擬態語",
        "categories": [
            {"id": cid, "name_zh": name, "icon": icon, "words": build(items)}
            for (cid, name, icon, items) in CATEGORIES
        ],
    }
    all_words = [w["word"] for c in data["categories"] for w in c["words"]]
    dups = {x for x in all_words if all_words.count(x) > 1}
    assert not dups, f"duplicate words: {dups}"
    path = os.path.join(os.path.dirname(__file__), "..", "src", "data", "onomatopoeia.json")
    with open(path, "w") as f:
        f.write(dumps_level(data))
    print(f"wrote {len(all_words)} words across {len(data['categories'])} categories")


if __name__ == "__main__":
    main()
